    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        EntityTransaction tx = em.getTransaction();
        tx.begin();
        Config config;
        if ((config = em.find(Config.class, 1L)) != null) {
            config.setId(1);
            config.setoAuthConsumerKey(consumerKey.getText());
            config.setoAuthConsumerSecret(consumerSecret.getText());
            config.setAccessToken(accessToken.getText());
            config.setAccessTokenSecret(accessTokenSecret.getText());
            config.setDebugEnabled(true);
            em.refresh(config);
            tx.commit();
            close();
        } else {   
            config = new Config();
            config.setId(1);
            config.setoAuthConsumerKey(consumerKey.getText());
            config.setoAuthConsumerSecret(consumerSecret.getText());
            config.setAccessToken(accessToken.getText());
            config.setAccessTokenSecret(accessTokenSecret.getText());
            config.setDebugEnabled(true);
            em.persist(config);
            tx.commit();
            close();
        }
    }//GEN-LAST:event_saveButtonActionPerformed

