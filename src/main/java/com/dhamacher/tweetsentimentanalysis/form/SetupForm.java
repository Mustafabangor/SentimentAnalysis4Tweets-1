    private void loadConfig() {
        EntityTransaction tx = em.getTransaction();
        tx.begin();
        Config c;
        if ((c = em.find(Config.class, 1L)) != null) {
            consumerKey.setText(c.getoAuthConsumerKey());
            consumerSecret.setText(c.getAccessTokenSecret());
            accessToken.setText(c.getAccessToken());
            accessTokenSecret.setText(c.getAccessTokenSecret());
            tx.commit();
        } else {
            consumerKey.setText("");
            consumerSecret.setText("");
            accessToken.setText("");
            accessTokenSecret.setText("");
            tx.commit();
        }
    }

